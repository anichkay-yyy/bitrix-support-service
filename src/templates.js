export const EXTRA_COPY_TEMPLATE = `Здравствуйте!

К уже запущенному в производство тиражу нельзя присоединить дополнительную книгу отдельным сообщением. Если требуется ещё один экземпляр, его нужно оформить как отдельный заказ.`;

export function looksLikeReprintRequest(text) {
  const raw = String(text ?? "").toLowerCase();
  if (/(?:брак|поврежд|слом|царап|порван|качество|вернуть\s+деньги|возврат\s+денег)/iu.test(raw)) return false;
  return /(?:перепеч|скидк[ауи]?\s*80|ошибк[аиуы]?.{0,80}(?:макет|фамил|фио|имя|школ|текст|фото|файл|облож)|неправильн.{0,80}(?:фамил|фио|имя|школ|текст|фото|файл|облож)|не\s+те\s+фото|опечатк|номер\s+школ|(?:отмен|измен).{0,80}(?:ошиб|макет|фамил|фио|имя|школ|текст|фото|файл|облож))/iu.test(
    raw,
  );
}

export function looksLikeExtraCopyRequest(text) {
  const raw = String(text ?? "").toLowerCase();
  return /(?:присоедин|добавить|дозаказ|еще\s+одн|ещё\s+одн|дополнительн).{0,120}(?:книг|альбом|тираж|заказ)/iu.test(
    raw,
  );
}
